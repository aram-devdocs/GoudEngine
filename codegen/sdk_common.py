def resolve_mixins(schema: dict) -> dict:
    """Expand mixin references in tool method lists.

    Tool methods may contain ``{"$ref": "MixinName", "name": "methodName"}``
    stubs.  This function replaces each stub with the full method definition
    from the referenced mixin, preserving the ordering of the tool's method
    array.  The ``mixins`` top-level key is removed after resolution so that
    generators see a flat, fully-resolved schema.
    """
    mixins = schema.get("mixins", {})
    if not mixins:
        return schema

    # Build a lookup: mixin_name -> {method_name -> method_def}
    mixin_lookup: dict[str, dict[str, dict]] = {}
    for mixin_name, mixin_def in mixins.items():
        mixin_lookup[mixin_name] = {
            m["name"]: m for m in mixin_def.get("methods", [])
        }

    for tool_def in schema.get("tools", {}).values():
        methods = tool_def.get("methods", [])
        resolved: list[dict] = []
        for m in methods:
            ref = m.get("$ref")
            if ref and ref in mixin_lookup:
                mixin_method = mixin_lookup[ref].get(m["name"])
                if mixin_method:
                    resolved.append(mixin_method)
                else:
                    resolved.append(m)
            else:
                resolved.append(m)
        tool_def["methods"] = resolved

    return schema


def load_schema() -> dict:
    with open(CODEGEN_DIR / "goud_sdk.schema.json") as f:
        schema = json.load(f)
    return resolve_mixins(schema)


def load_repo_version() -> str:
    """Read the canonical repo version from the Rust core crate manifest."""
    cargo_toml = (ROOT_DIR / "goud_engine" / "Cargo.toml").read_text()
    match = re.search(r'^version = "([^"]+)"', cargo_toml, re.MULTILINE)
    if not match:
        raise ValueError("Could not determine repo version from goud_engine/Cargo.toml")
    return match.group(1)


def load_ffi_manifest() -> dict[str, dict]:
    """Load the flat ffi_manifest.json function metadata."""
    manifest_path = CODEGEN_DIR / "ffi_manifest.json"
    if not manifest_path.exists():
        raise FileNotFoundError(
            "ffi_manifest.json not found. Run `cargo build` to generate it."
        )

    payload = json.loads(manifest_path.read_text())
    if not isinstance(payload, dict):
        raise ValueError("ffi_manifest.json must be a JSON object")

    functions = payload.get("functions", {})
    if not isinstance(functions, dict):
        raise ValueError("ffi_manifest.json 'functions' section must be an object")
    return functions


def _manifest_param_to_mapping(param: str | dict) -> dict:
    """Convert a manifest parameter entry into ffi_mapping param metadata."""
    if isinstance(param, dict):
        name = str(param.get("name", "")).strip()
        ffi_type = str(param.get("type", "")).strip()
        if not name or not ffi_type:
            raise ValueError(f"Invalid manifest parameter object: {param!r}")
        return {"name": name, "type": normalize_ffi_type(ffi_type)}

    if not isinstance(param, str):
        raise ValueError(f"Unsupported manifest parameter entry: {param!r}")

    name, sep, ffi_type = param.partition(":")
    if not sep:
        raise ValueError(f"Manifest parameter missing type annotation: {param!r}")
    return {"name": name.strip(), "type": normalize_ffi_type(ffi_type.strip())}


def normalize_ffi_type(raw_type: str) -> str:
    """Normalize manifest-emitted aliases into stable codegen types."""
    ffi_type = raw_type.strip()
    alias = _FFI_TYPE_ALIASES.get(ffi_type)
    if alias is not None:
        return normalize_ffi_type(alias)

    if ffi_type.startswith("Option<") and ffi_type.endswith(">"):
        inner = normalize_ffi_type(ffi_type[len("Option<") : -1])
        return f"Option<{inner}>"

    if ffi_type.startswith("*mut ") or ffi_type.startswith("*const "):
        qualifier, inner = ffi_type.split(" ", 1)
        return f"{qualifier} {normalize_ffi_type(inner)}"

    return ffi_type


def _materialize_ffi_function_signatures(mapping: dict) -> dict:
    """Hydrate ffi function signatures from ffi_manifest.json."""
    manifest = load_ffi_manifest()
    resolved = copy.deepcopy(mapping)

    for module_name, module_def in resolved.get("ffi_functions", {}).items():
        if module_name == "_comment" or not isinstance(module_def, dict):
            continue

        for fn_name, fn_meta in module_def.items():
            if fn_name.startswith("_") or not isinstance(fn_meta, dict):
                continue

            manifest_entry = manifest.get(fn_name)
            if manifest_entry is None and fn_meta.get("alias_of"):
                manifest_entry = manifest.get(fn_meta["alias_of"])
            if manifest_entry is None:
                raise ValueError(
                    f"ffi_mapping.json references '{fn_name}' but ffi_manifest.json does not"
                )

            materialized_meta = {
                k: v for k, v in fn_meta.items()
                if k not in {"params", "returns", "unsafe"}
            }
            materialized_meta["params"] = [
                _manifest_param_to_mapping(param)
                for param in manifest_entry.get("params", [])
            ]
            materialized_meta["returns"] = normalize_ffi_type(
                manifest_entry.get("return_type", "void")
            )
            if manifest_entry.get("is_unsafe"):
                materialized_meta["unsafe"] = True

            module_def[fn_name] = materialized_meta

    return resolved


def _materialize_legacy_ffi_view(
    schema: dict,
    mapping: dict,
    schema_key: str,
    legacy_key: str,
    item_label: str,
) -> dict:
    """Project schema-owned FFI metadata into the historical mapping view."""
    resolved = copy.deepcopy(mapping)
    schema_section = schema.get(schema_key, {})
    if not schema_section:
        return resolved

    existing_section = resolved.pop(legacy_key, None)
    if existing_section is not None and existing_section != schema_section:
        raise ValueError(
            f"Conflicting {item_label} metadata between goud_sdk.schema.json "
            f"({schema_key}) and ffi_mapping.json ({legacy_key})"
        )

    resolved[legacy_key] = copy.deepcopy(schema_section)
    return resolved


def load_ffi_mapping(schema: dict | None = None) -> dict:
    with open(CODEGEN_DIR / "ffi_mapping.json") as f:
        mapping = json.load(f)

    resolved_schema = schema if schema is not None else load_schema()
    resolved_mapping = _materialize_ffi_function_signatures(mapping)
    resolved_mapping = _materialize_legacy_ffi_view(
        resolved_schema,
        resolved_mapping,
        "ffi_tools",
        "tools",
        "ffi tool",
    )
    return _materialize_legacy_ffi_view(
        resolved_schema,
        resolved_mapping,
        "ffi_type_methods",
        "type_methods",
        "ffi type method",
    )


# ── Naming conventions ──────────────────────────────────────────────


def to_pascal(name: str) -> str:
    """camelCase or PascalCase -> PascalCase"""
    if not name:
        return name
    return name[0].upper() + name[1:]


def to_snake(name: str) -> str:
    """camelCase -> snake_case"""
    s1 = re.sub(r"([A-Z]+)([A-Z][a-z])", r"\1_\2", name)
    s2 = re.sub(r"([a-z\d])([A-Z])", r"\1_\2", s1)
    return s2.lower()


def to_screaming_snake(name: str) -> str:
    """camelCase -> SCREAMING_SNAKE_CASE"""
    return to_snake(name).upper()


def to_camel(name: str) -> str:
    """Already camelCase in schema, but ensure first char is lower."""
    if not name:
        return name
    return name[0].lower() + name[1:]


