#!/usr/bin/env python3
"""Generate SDK README.md files from a shared template and per-language config.

Usage:
    python3 codegen/gen_sdk_readmes.py          # generate all SDK READMEs
    python3 codegen/gen_sdk_readmes.py --check   # verify READMEs are up-to-date
"""
from __future__ import annotations

import json
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
CONFIG_PATH = REPO_ROOT / "codegen" / "sdk_readme_config.json"
SNIPPETS_DIR = REPO_ROOT / "codegen" / "sdk_readme_snippets"

HEADER = "<!-- This file is AUTO-GENERATED by gen_sdk_readmes.py. DO NOT EDIT. -->"

# Map snippet file extensions to markdown language identifiers
LANG_MAP = {
    ".rs": "rust",
    ".cs": "csharp",
    ".py": "python",
    ".ts": "typescript",
    ".c": "c",
    ".cpp": "cpp",
    ".go": "go",
    ".kt": "kotlin",
    ".swift": "swift",
    ".lua": "lua",
}


def load_config() -> dict:
    with open(CONFIG_PATH) as f:
        return json.load(f)


def load_snippet(filename: str) -> str:
    path = SNIPPETS_DIR / filename
    return path.read_text().rstrip("\n")


def snippet_lang(filename: str) -> str:
    ext = Path(filename).suffix
    return LANG_MAP.get(ext, "")


def generate_readme(sdk_key: str, cfg: dict) -> str:
    lines: list[str] = []
    lines.append(HEADER)
    lines.append("")

    # Title
    lines.append(f"# {cfg['title']}")
    lines.append("")

    # Badges
    if cfg.get("badges"):
        lines.append(" ".join(cfg["badges"]))
        lines.append("")

    # Alpha warning
    lines.append(
        "> **Alpha** -- This SDK is under active development. "
        "APIs change frequently. "
        "[Report issues](https://github.com/aram-devdocs/GoudEngine/issues)"
    )
    lines.append("")

    # Description
    lines.append(cfg["description"])
    lines.append("")

    # Installation
    lines.append("## Installation")
    lines.append("")
    if cfg.get("install_command"):
        lines.append("```bash")
        lines.append(cfg["install_command"])
        lines.append("```")
        lines.append("")
    if cfg.get("install_label"):
        lines.append(cfg["install_label"])
        lines.append("")
    if cfg.get("install_from_source"):
        lines.append("Requires the native engine library. Build it first:")
        lines.append("")
        lines.append("```bash")
        lines.append(cfg["install_from_source"])
        lines.append("```")
        lines.append("")

    # Quick Start
    lines.append("## Quick Start")
    lines.append("")
    snippet = load_snippet(cfg["quick_start_file"])
    lang = snippet_lang(cfg["quick_start_file"])
    lines.append(f"```{lang}")
    lines.append(snippet)
    lines.append("```")
    lines.append("")

    # Documentation
    lines.append("## Documentation")
    lines.append("")
    gs_path = cfg["getting_started_path"]
    lines.append(
        f"See the [Getting Started guide](../../docs/src/getting-started/{gs_path}) "
        "for installation, project setup, and examples."
    )
    lines.append("")

    # Testing
    lines.append("## Testing")
    lines.append("")
    lines.append("```bash")
    lines.append(cfg["test_command"])
    lines.append("```")
    lines.append("")

    # Platform Support
    if cfg.get("platform_support"):
        lines.append("## Platform Support")
        lines.append("")
        header = cfg.get("platform_header", ["OS", "Architecture", "Status"])
        lines.append(f"| {header[0]} | {header[1]} | {header[2]} |")
        lines.append("|" + "|".join(["----"] * 3) + "|")
        for p in cfg["platform_support"]:
            lines.append(f"| {p['os']} | {p['arch']} | {p['status']} |")
        lines.append("")

    # Architecture
    lines.append("## Architecture")
    lines.append("")
    lines.append(cfg["architecture_note"])
    lines.append("")

    # Links
    lines.append("## Links")
    lines.append("")
    gs_path = cfg["getting_started_path"]
    lines.append(
        f"- [Full documentation](../../docs/src/getting-started/{gs_path})"
    )
    if cfg.get("examples_url"):
        lines.append(f"- [Examples]({cfg['examples_url']})")
    lines.append(
        "- [License: MIT](https://github.com/aram-devdocs/GoudEngine/blob/main/LICENSE)"
    )
    lines.append("")
    return "\n".join(lines)


def main():
    check_mode = "--check" in sys.argv
    config = load_config()
    all_ok = True

    for sdk_key, cfg in config["sdks"].items():
        sdk_dir = REPO_ROOT / cfg["sdk_dir"]
        readme_path = sdk_dir / "README.md"
        expected = generate_readme(sdk_key, cfg)

        if check_mode:
            if not readme_path.exists():
                print(f"MISSING: {readme_path}")
                all_ok = False
                continue
            actual = readme_path.read_text()
            if actual != expected:
                print(f"OUT OF DATE: {readme_path}")
                all_ok = False
            else:
                print(f"OK: {readme_path}")
        else:
            readme_path.write_text(expected)
            print(f"Generated: {readme_path}")

    if check_mode and not all_ok:
        print("\nSome READMEs are out of date. Run: python3 codegen/gen_sdk_readmes.py")
        sys.exit(1)


if __name__ == "__main__":
    main()
