/**
 * Creates and initialises a web-based GoudGame instance.
 *
 * This is the primary entry point for the web backend. It:
 * 1. Loads the wasm module (if not already loaded)
 * 2. Creates or reuses a canvas element
 * 3. Instantiates the Rust `WasmGame`
 * 4. Returns a fully wired `GoudGame`
 */
export async function createWebGame(config: WebGameConfig = {}): Promise<GoudGame> {
  await initWasm(config.wasmUrl);

  const wasm = getWasm();
  const width = config.width ?? 800;
  const height = config.height ?? 600;
  const title = config.title ?? 'GoudEngine';

  let canvas = config.canvas ?? null;
  if (!canvas) {
    canvas = document.createElement('canvas');
    canvas.width = width;
    canvas.height = height;
    canvas.style.display = 'block';
    document.body.appendChild(canvas);
  } else {
    canvas.width = width;
    canvas.height = height;
  }

  document.title = title;

  const handle = new wasm.WasmGame(width, height, title);

  const resizeObserver = new ResizeObserver((entries) => {
    for (const entry of entries) {
      const { width: w, height: h } = entry.contentRect;
      handle.set_canvas_size(Math.round(w), Math.round(h));
    }
  });
  resizeObserver.observe(canvas);

  return new GoudGame(handle, canvas);
}
