/**
 * Web backend for GoudEngine SDK.
 *
 * Loads the Wasm module produced by `wasm-pack`, creates a canvas,
 * wires browser input events into the engine, and drives the game
 * loop via `requestAnimationFrame`.
 *
 * Usage:
 * ```ts
 * import { createWebGame } from '@goudengine/sdk/web';
 *
 * const game = await createWebGame({
 *   title: 'My Game',
 *   width: 800,
 *   height: 600,
 *   canvas: document.getElementById('canvas') as HTMLCanvasElement,
 * });
 *
 * const player = game.spawnEmpty();
 * game.addTransform2d(player, { positionX: 100, positionY: 200, rotation: 0, scaleX: 1, scaleY: 1 });
 *
 * game.run((ctx) => {
 *   // per-frame update
 * });
 * ```
 */

